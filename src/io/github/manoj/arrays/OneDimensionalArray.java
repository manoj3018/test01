package io.github.manoj.arrays;

public class OneDimensionalArray {
	
	public static void main(String[] args) {
		
		int oned_array[] = new int[4];
		
		oned_array[0] = 51;
		oned_array[1] = 35;
		oned_array[2] = 52;
		oned_array[3] = 15;
		
		System.out.println("Element at index 0 :" + oned_array[0]);
		System.out.println("Element at index 1 :" + oned_array[1]);
		System.out.println("Element at index 2 :" + oned_array[2]);
		System.out.println("Element at index 3 :" + oned_array[3]);
	}

}
